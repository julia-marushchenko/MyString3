/**
 *  Java program to use charAt() method for String.
 */

package com.strings;

/**
 *  Main class.
 */
public class Main {

    // Main class to run java program.
    public static void main(String[] args) {

        // Creating string.
        String letters = "ABCDEFGHIJKLMN";

        // Finding index of symbol 'GHI'
        System.out.println(letters.indexOf("GHI")); // Output: 6, because of index of first character 'G'.

    }
}